# CT1: 세율표 경계값 검증 — 2억/200억/3,000억/3,000억초과
# ══════════════════════════════════════════════

def scenario_ct1_tax_rate_boundaries():
    """CT1: 세율표 경계값 검증 (§55)

    과세표준 2억:    2억 × 9% = 18,000,000
    과세표준 2억+1:  200,000,001 × 19% - 20,000,000 = 18,000,000
    과세표준 200억:  200억 × 19% - 2,000만 = 37억 8,000만 = 3,780,000,000
    과세표준 3,000억: 3,000억 × 21% - 4.2억 = 625.8억 = 62,580,000,000
    과세표준 5,000억: 5,000억 × 24% - 94.2억 = 1,105.8억 = 110,580,000,000
    """
    # 2억 경계 (9% 최대)
    r = apply_corporate_tax_rate(taxable_base=200_000_000)
    assert r['산출세액'] == 18_000_000, f"2억 세액 {r['산출세액']} != 18,000,000"
    assert r['적용세율'] == 0.09

    # 2억+1원 (19% 진입)
    r = apply_corporate_tax_rate(taxable_base=200_000_001)
    expected = int(200_000_001 * 0.19) - 20_000_000
    assert r['산출세액'] == expected, f"2억+1 세액 {r['산출세액']} != {expected}"
    assert r['적용세율'] == 0.19

    # 200억 경계 (19% 최대)
    r = apply_corporate_tax_rate(taxable_base=20_000_000_000)
    assert r['산출세액'] == 3_780_000_000, f"200억 세액 {r['산출세액']} != 3,780,000,000"

    # 200억+1원 (21% 진입)
    r = apply_corporate_tax_rate(taxable_base=20_000_000_001)
    expected = int(20_000_000_001 * 0.21) - 420_000_000
    assert r['산출세액'] == expected, f"200억+1 세액 {r['산출세액']} != {expected}"
    assert r['적용세율'] == 0.21

    # 3,000억 경계 (21% 최대)
    r = apply_corporate_tax_rate(taxable_base=300_000_000_000)
    assert r['산출세액'] == 62_580_000_000, f"3,000억 세액 {r['산출세액']} != 62,580,000,000"

    # 5,000억 (24%)
    r = apply_corporate_tax_rate(taxable_base=500_000_000_000)
    assert r['산출세액'] == 110_580_000_000, f"5,000억 세액 {r['산출세액']} != 110,580,000,000"

    # 0원 이하
    r = apply_corporate_tax_rate(taxable_base=0)
    assert r['산출세액'] == 0

    r = apply_corporate_tax_rate(taxable_base=-100)
    assert r['산출세액'] == 0

    print("  CT1 PASS: 세율표 경계값 전부 일치")


